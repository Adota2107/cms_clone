    /** 
     * Handles the HTTP <code>GET</code> method.
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
        HttpSession session = request.getSession();
        
        CoursesDAO cDAO = new CoursesDAO();
        StudentsDAO sDAO = new StudentsDAO();
        
        //Check if user login
        if (session.getAttribute("checkLogin") != null) {
            String acc = session.getAttribute("acc").toString();
            String id = sDAO.getIDbyAccount(acc);
            ArrayList<Courses> courses = cDAO.getListCoursebyId(id);
            request.setAttribute("courses", courses);
        }
        
        ArrayList<Categories> cate = cDAO.getListCate();
        
        request.setAttribute("cate", cate);
        
        request.getRequestDispatcher("courses.jsp").forward(request, response);
    }

